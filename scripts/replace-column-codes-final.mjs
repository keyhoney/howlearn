function finalFix(content) {
  let s = content;
  for (const [from, to] of GRAMMAR) {
    s = s.split(from).join(to);
  }
  for (let n = 45; n >= 1; n--) {
    s = s.replace(new RegExp(`C${n}`, 'g'), L(n));
  }
  s = s.replace(/글 글/g, '글');
  s = s.replace(/루프 루프/g, '루프');
  s = s.replace(/블록 블록/g, '블록');
  return s;
}

const files = fs.readdirSync(columnsDir).filter((f) => f.endsWith('.mdx'));
let changed = 0;
const leftovers = [];

for (const file of files) {
  const fp = path.join(columnsDir, file);
  const original = fs.readFileSync(fp, 'utf8');
  const updated = finalFix(original);
  if (updated !== original) {
    fs.writeFileSync(fp, updated, 'utf8');
    changed++;
  }
  const m = updated.match(/C\d{1,2}/g);
  if (m) leftovers.push({ file, codes: [...new Set(m)] });
}

console.log(`Final fix: ${changed} files`);
if (leftovers.length) console.log(JSON.stringify(leftovers, null, 2));
else console.log('No C-codes remaining');
